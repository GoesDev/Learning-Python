# 2.4 Letras maiúsculas e minúsculas em nomes: Armazene o nome
# de uma pessoa em uma variável e então apresente o nome dessa
# pessoa em minúsculas, em letras maiscúlas e somente com a
# primeira letra maiúscula.

nome = "goes"
